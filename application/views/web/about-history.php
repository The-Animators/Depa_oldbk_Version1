                <div class="col-md-12 align-justify" >

<p>	દરેક પ્રજાને પોતાનો ઇતિહાસ હોય છે. ઇતિહાસના અભ્યાસથી વ્યકિત કે સમૂહનું પ્રચીન સ્વરૂપ જાણી શકાય છે. આપણે જૈનો ઓસવાળ જ્ઞાતિના છીએ.</p>
<p>	ઓસવાળ સમાજ મૂળ રાજપુત-ક્ષત્રિઓનો બનેલો છે. જૈન સાધુઓએ જે ક્ષત્રિયોને પ્રતિબોધી જૈન બનાવ્યા તે ક્ષત્રિયો ઓસવાળ સમાજમાં દાખલ થયા. તેઓ પ્રથમ મહાજન અને પછીથી ઓસવાલ તરીકે ઓળખાયા. ઓસવાલ જ્ઞાતિનું નામ તેના "ઓસિયા' નગર પરથી પડેલું છે. જે મારવાડમાં જોધપુરની ઉત્તરે પંદર ગાઉના અંતરે આવેલું હતું. ઓસવાલો મુખ્યત્વે સોલંકી, પરમાર, રાઠોડ, ભટ્ટી, ચોહાણ અને ચાવડા વંશના છે.</p>
<p>	કચ્છી વિશા ઓસવાળ જૈનોની વસતિ કચ્છભરમાં 118 ગામમાં ગણાય છે. કંઠી પ્રદેશના પર ગામ, અબડાસાના 42 ગામ અને વગડના 24 ગામ ગણવામાં આવે છે. કચ્છી વિશા ઓસવાળ જૈનોની સંખ્યા લગભગ પોણા બે લાખની છે.</p>
<p>	એસિયાના આસપાસના પ્રદેશમાં પહેલા સારી જાહોજલાલી હતી. ખેતી અને પશુપાલન મુખ્ય વ્વવસાય હતા. અપૂરતો વરસાદ અને વિધર્મીઓના આક્રમણથી બચવા ઓસવાળોએ મારવાડથી થર પારકંર આવ્યા. (જે કચ્છ અને સિંધની સરહદ પર છે અને હાલ પાકિસ્તાનમાં છે.) ત્યાંથી તેઓ વિક્રમ સંવત 1565ની આસપાસ (આજથી પોણા પાંચસો વર્ષ પહેલાં) કચ્છમાં વાગડમાં આવ્યા.</p>
<p>	જેમ જેમ સંખ્યા વધતા લાગી તેમ તેઓ કચ્છના અંદરના ભાગમાં વિસ્તરતા ગયા. યોગ્ય સ્થળોએ તોરણ બાંધી નવાગામ વસાવવા લાગ્યા. કચ્છના રાજવીઓએ એમને માનપાન સાથે સત્કારી ગામડાઓમાં જમીન - રહેઠાણ વગેરેની સગવડ આપી.</p>
<p>	રાજાની ગાદીનો વારસ બનનાર પાટવીકુંવર કહેવાતો અને અન્ય રાજકુમારોને ઉચ્ચ હોદ્રાઓ કે કોઇ જાગીરનો વહીવટ સેંપવામાં આવતો. ટોભાસરના દીપુભા ઠકરાઇએ વસાવેલ ગામ તે આપણું પ્રિય ડેપા ગામ. ડેપા ગામનું તોરણ લધા પાસુની "સાવલા' ડેલીમાં બંધાયું હતું.  ડેપા ગામ વસાવનાર "દેવુભા' નામ લાડનું નામ હતું. તેમનું મૂળ નામ "દેવપાળ' હતું. જેના પરથી કચ્છીમાં "દેપાર' થયું. તેમાંથી "દેપા'  થયું. હાલ આપણે "દેપા' કે "ડેપા' કહીએ છીએ.</p>
<p>	ડેપા એક સુંદર મજાનું નાનકડું ગામ છે. જેમાં જૈનોની વસતિ નેંધપાત્ર સંખ્યા છે. એક સમયે કચ્છમાં જૈનોને મુખ્ય વ્યવસાય પણ ખેતીવાડીનો હતો. ડેપાના જૈનોની પણ ખેતીવાડી હતી. સાથોસાથ મુંબઇમાં પણ ધંધા રરિજગાર માટે સ્થિર થતા ગયા. વિશાળ મકાનો અને ઊંચી મેડીઓ એક સમયના ધબકતા આપણા વસવાટની સાખ પુરે છે.</p>
<p>	ડેપા ગામમાં વસીએ કે દેશ દેશાવરમાં વસવાટ કરી પણ માતૃભૂમિ કદિ ભૂલાતી નથી. જેમણે જેમણે ડેપાની ધુળનો શીતળ સ્પર્શ થયો હશે, જેમણે ડેપાના તળાવનું મનોહર દશ્ય જોયું હશે તેઓ ડેપાને ભૂલી નહિ શકે.</p>
<p>	સાંજે સીમમાંથી ચરીને પાછી ફરતી ગાયોને જોઇએ છીએ ત્યારે મેં માંથી શબ્દ સરી પડે છેઃ</p>
<p>“કારા ડુંગર કચ્છજા, ધોરા ધણ ચરેં,</p>
<p>સિજ ઉલે સામાં મલે, ડિસી દુઃખ ટરેં' “</p>
