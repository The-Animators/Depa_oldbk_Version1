</p><p>
ગામની પૂર્વે જતાં પાણીનો ટાંકો આવેલ છે જૂનો ટાંકો ભૂકંપ દરમ્યાન ક્ષતિગ્રસ્ત થતાં શ્રી ડેપા સર્વ સેવા સંઘ ના કાર્યકરો ના પ્રયાસ થી નવો ટાંકો "ચેસવી"નામક ઇટાલી ની સંસ્થા દ્વારા નિર્માણ કરી આપવામાં આવેલ જેનું ઉદ્દઘાટન ગામના પ્રજ્ઞાચક્ષુ શ્રી વેલજી શિવજી ફુરીયા (વેલા સવું)ના વરદહસ્તે કરવામાં આવેલ જેના દ્વારા ગામને નળ વાટે પાણી પુરૂ પાડવામાં આવેછે
બાજુમાં બાપા સીતારામ ની મઢુલી આવેલ છે સામે આંગણવાડી આવેલ છે જે ભૂકંપ દરમ્યાન ક્ષતિગ્રસ્ત થતાં શ્રી ડેપા સર્વ સેવા સંઘ ના કાર્યકરોના પ્રયાસથી "ચેસવી"ઇટાલિયન સંસ્થા દ્વારા નિર્માણ કરી આપવામાં આવેલ છે આગળ વધીએ ત્યાં મુખ્ય માર્ગ ના કિનારે શ્રી કરમશી જીવણ બસ સ્ટેન્ડ અને શ્રી વેરશી નથુભાઈ સાવલા પાણીનું પરબ અને બાજુમાં ૨૦૦ વર્ષ જૂનું આમલી નું વૃક્ષ આવેલ છે 
સામે  સડક ના કિનારે "સુવિધા નિરણ કેન્દ્ર" (વથાણ) મુખ્ય દાતા માતુશ્રી મુરીબેન ડુંગરશી ધારશી મારૂ (સુવિધા પરિવાર) નિરણ ભંડાર ના દાતા માતુશ્રી લક્ષ્મીબેન લાલજી તેજશી સાવલા અને આધાર સ્તંભ દાતાઓ હેમલતાબેન મણિલાલ પ્રેમજી રાંભિયા ,પ્રભાબેન દેવશી વિજપાર મારૂ ,ચંચલબેન ધનજી કરમશી મામણિયા તરફથી ગામના પશુધન ને દરેક ઋતુ માં શાતા ઉપજે એવું વથાણ સ્વપ્નદ્રષ્ટા વડીલ શ્રી માવજી મુરજી છેડા અને ગૌસેવાપ્રેમી શ્રી નવીન (જીતુભાઇ)મણિલાલ સાવલા,શ્રી ભુરૂભા  શવરાજજી જાડેજા અને એક સદગ્રહસ્ત ભુમીદાતાના સહયોગ વડે આકાર પામ્યું છે જેનું ઉદઘાટન દાતા પરિવાર,મહાનુભવો,મહેમાનો તથા ગામવાસીઓ ની હાજરીમાં તા.૨-૭-૨૦૧૭ માં સંપન્ન થયેલ.મુખ્ય સડક પર આગળ વધીએ તો દાતા પરિવાર સ્વ.લક્ષ્મીચંદ પ્રેમજી રાંભિયા ના સ્મરણાર્થે શ્રીમતી હેમલતાબેન મણિલાલ પ્રેમજી  રાંભિયા પરિવાર તરફથી સં ૨૦૧૫ બંધાવી આપેલ પાણીનો હવાડો જે ગામના પશુધન,જતા આવતા વટેમાર્ગુ અને માલધારીઓ માટે ખુબજ ઉપયોગી સાબિત થઈ રહયુ છે બાજુમાં નવી વાવ આવેલ છે 
સામે શ્રી ડેપા સર્વોદય ટ્રસ્ટ દ્વારા સંચાલિત અતિથિગ્રહ (સેનેટોરિયમ) મુખ્ય દાતા માતુશ્રી નાનબાઈ વેરસી નથુ સાવલા પરિવાર ,આધાર સ્તંભ દાતા માતુશ્રી મુરીબાઈ ડુંગરશી ધારશી મારૂ પરિવાર અને વિંગ દાતા માતુશ્રી પાનબાઈ લખમશી દેવજી રાંભિયા પરિવાર અને ગામવાસીઓ ના સહયોગથી સ.૨૦૦૨ માં બનાવી આપેલ છે બાજુમાં ભોજનશાળા માટે વીશાળ જગ્યા  માતુશ્રી મુરીબાઈ ડુંગરશી ધારશી મારૂ પરિવાર તરફથી વપરાશ અર્થે મળેલ છે
ગામની દક્ષિણે શ્રી ડેપા પંચાયત પ્રાથમિક શાળા નું મકાન આવેલ છે જે ભૂકંપ દરમ્યાન ક્ષતિગ્રસ્ત થતાં શ્રી ડેપા સર્વ સેવા સંઘ ના કાર્યકરો ના પ્રયાસ થકી "ચેસવી" ઇટાલી ની સંસ્થા દ્વારા બનાવી આપવામાં આવેલ છે  બાજુમાં શ્રી ડેપા સર્વ સેવા સંઘ ના કાર્યકરો ના પ્રયાસ અને ભુમીદાતા  માતુશ્રી દેવકાંબેન વેલજી માયા મારૂ અને માતુશ્રી રાંણબાઈ તલકશી ખીમજી મારૂ પરિવાર અને દાતા શ્રી યુનાઇટેડ વે ઓફ બરોડા દ્વારા બનાવવામાં આવેલ કોમ્યુનિટી હોલ (વિવિધલક્ષી હોલ)આવેલ છે સ્કૂલ ની બાજુમાં હનુમાનની ડેરી અને મફત નગરી ના મકાનો આવેલ છે
મોટી ખાખર તરફ જતાં મુખ્ય સડક પર દક્ષિણ બાજુ સ્મશાન ભૂમિ આવેલ છે
ડેપા ગામનો વિસ્તાર ઘણો નાનો છે પણ ગામની ભૂમિ રળિયામણી છે લોકોમાં સ્નેહ, સદભાવના ની સુવાસ છે દરેક જ્ઞાતિ ના લોકો સુખ અને સંપ થી રહે છે ગૌરવગાથા છે.</p>

